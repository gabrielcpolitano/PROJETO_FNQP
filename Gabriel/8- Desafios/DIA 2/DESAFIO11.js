/* 
11. Operadores lógicos
    Declare três variáveis booleanas e use os operadores &&, || e ! para verificar diferentes
    combinações. Exiba os resultados.
*/

const valor1 = true;
const valor2 = false;
const valor3 = true;


console.log(valor1 && valor3);
console.log(valor1 || valor2);

console.log(!valor1);
console.log(!valor2);
console.log(!valor3);