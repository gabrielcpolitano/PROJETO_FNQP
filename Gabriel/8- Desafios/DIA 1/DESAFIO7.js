/* 
7. Comparações simples
   Declare duas variáveis numéricas e exiba no console o resultado das comparações: >, <, >=, <=,
   ==, ===, !=, !==.
*/

const num1 = 2
const num2 = 3

console.log(num1 > num2);
console.log(num1 < num2);
console.log(num1 >= num2);
console.log(num1 <= num2);
console.log(num1 == num2);
console.log(num1 === num2);
console.log(num1 != num2);
console.log(num1 !== num2);
