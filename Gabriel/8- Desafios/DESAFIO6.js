/* 
6. Operadores de atribuição
   Defina uma variável com valor inicial 50. Atualize seu valor usando os operadores +=, -=, *=, e /=
   e exiba os resultados.
*/

let num = 50;
console.log(num);
num += 1;
console.log(num);
num -= 11;
console.log(num);
num *= 2;
console.log(num);
num /= 4;
console.log(num);